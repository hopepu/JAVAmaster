package ch10.com.section03;

public class Package03 {
	public static void main(String[] args) {
		System.out.println(Math.pow(2, 2));
		System.out.println(Math.ceil(9.9));
		System.out.println(Math.floor(9.9));
		System.out.println(Math.round(9.9));
		System.out.println(Math.max(5, 100));
		System.out.println(Math.min(5, 100));
		
	}
}
